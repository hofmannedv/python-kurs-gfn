# Listen festlegen

preise = [10, 5, 6, 3, 17]
listeGemischt = ["Freiburg", 79117, 145338.4]

# jedes Element einzeln ausgeben
for element in preise:
	print(element)

# ganze Liste ausgeben
print(preise)

# Typ ausgeben: Liste
print(type(preise))

# Liste durchgehen und Typ jeden Elements ausgeben
for element in listeGemischt:
    print(type(element))
