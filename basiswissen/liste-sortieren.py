# 2 listen festlegen
lottozahlen1 = [7, 23, 42]
lottozahlen2 = [12, 8, 16]

# zwei Listen vereinigen
lottozahlen = lottozahlen1 + lottozahlen2
print(lottozahlen1)
print(lottozahlen2)
print(lottozahlen)

# gibt sortiertes Ergebnis zurueck
lottozahlenSortiert = sorted(lottozahlen)

# sortiert liste direkt
lottozahlen.sort()

print(lottozahlenSortiert)
print(lottozahlen)
